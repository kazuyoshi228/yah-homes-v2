// 星評価（塗り分けSVG・旧 StarRating と同ロジック）
export function starRating(rating: number, size = 12, idPrefix = "star"): string {
  const full = Math.floor(rating);
  const partial = rating - full;
  const stars = [1, 2, 3, 4, 5]
    .map((i) => {
      const fill = i <= full ? 1 : i === full + 1 && partial > 0 ? partial : 0;
      const gid = `${idPrefix}-${i}-${String(rating).replace(".", "_")}`;
      return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" style="flex-shrink:0"><defs><linearGradient id="${gid}"><stop offset="${fill * 100}%" stop-color="#111111"/><stop offset="${fill * 100}%" stop-color="#dddddd"/></linearGradient></defs><polygon points="12,2 15.09,8.26 22,9.27 17,14.14 18.18,21.02 12,17.77 5.82,21.02 7,14.14 2,9.27 8.91,8.26" fill="url(#${gid})" stroke="#111111" stroke-width="1.5"/></svg>`;
    })
    .join("");
  return `<span style="display:inline-flex;gap:1px;align-items:center">${stars}</span>`;
}
