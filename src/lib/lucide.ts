// stroke アイコン。size / strokeWidth / color を旧実装の既定値に合わせて出力する。

function svg(inner: string, size: number, strokeWidth: number, color: string): string {
  return `<svg width="${size}" height="${size}" viewBox="0 0 24 24" fill="none" stroke="${color}" stroke-width="${strokeWidth}" stroke-linecap="round" stroke-linejoin="round" aria-hidden="true">${inner}</svg>`;
}

