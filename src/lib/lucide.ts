export function lucide(
  name: keyof typeof PATHS | string,
  opts: { size?: number; strokeWidth?: number; color?: string } = {}
): string {
  const inner = PATHS[name as string];
  if (!inner) return "";
  return svg(inner, opts.size ?? 16, opts.strokeWidth ?? 1.5, opts.color ?? "currentColor");
}

